            <h2>Punteggio e livelli:</h2>
            <p>Ogni riga completata ti assegna un punteggio base proporzionale al livello attuale. Il tuo punteggio sarà calcolato moltiplicando il punteggio base, derivante dal numero di righe completate, per il livello attuale.</p>
            <ul>
                <li><strong>1 riga completata:</strong> 40 punti</li>
                <li><strong>2 righe contemporanee:</strong> 100 punti</li>
                <li><strong>3 righe contemporanee:</strong> 300 punti</li>
                <li><strong>4 righe contemporanee (Tetris):</strong> 1200 punti</li>
            </ul>
            <p>Il tuo livello aumenta ogni 10 righe completate. Ogni nuovo livello comporta una caduta più veloce dei blocchi. Inoltre, quando usi la freccia giù e il blocco si sposta verso il basso, ottieni un punto aggiuntivo nel punteggio.<br>
                Inoltre, ogni 100 punti ottenuti in partita, otterrai 1$ da spendere nello shop.
